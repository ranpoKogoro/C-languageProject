// HTMLを表示する
#include <stdio.h>
int main(void){
    printf("<h1>hello world</h1>\n");
    printf("<p>世界の皆さん、\n");
    printf("<b>こんにちは</b</p>");
    printf("<h1>hello world</h1>\n");
    printf("<p>世界の皆さん、<br>\n");
    printf("<b>こんにちは</b</p>");
    // \nはあくまでCのコンソール上で改行されるhtmlを改行するためには、<br>を利用する
}
